// ============================================================================

using namespace BFC;

// ============================================================================

IO::AutoCloser::~AutoCloser() {

	if ( device ) {
		try {
			device->close();
		}
		catch ( Exception & e ) {
			msgExc( e, "Can't auto-close!" );
		}
	}

}

