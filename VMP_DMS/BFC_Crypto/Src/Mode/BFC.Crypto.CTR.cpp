Crypto::CTR::CTR(
	const	Bool		pCtrBE ) :

	Mode( getClassType() ),
	ctrBE( pCtrBE ) {

}

// ============================================================================

void Crypto::CTR::init(
		CipherPtr	pCipher,
	const	Buffer &	pIV,
	const	Buffer &	pKey ) {

	if ( pCipher->getBlockSize() % 8 ) {
		throw InternalError( "Spurious block length!" );
	}

	if ( pIV.getLength() != pCipher->getBlockSize() ) {
		throw InvalidArgument( "Invalid initial vector!" );
	}

	cipher	= pCipher;
	blkSze	= pCipher->getBlockSize();
	ctrBuf	= pIV;

	cipher->setKey( pKey );

}

void Crypto::CTR::done() {

	if ( ! cipher ) {
		throw NotInitialized();
	}

	cipher.kill();

}

// ============================================================================

Buffer Crypto::CTR::encrypt(
	const	Buffer &	pPlainText ) {

	if ( ! cipher ) {
		throw NotInitialized();
	}

	Uint32 len = pPlainText.getLength();

	if ( len % blkSze ) {
		throw InvalidArgument();
	}

	Buffer res;

	res.resize( len );

	for ( Uint32 i = 0 ; i < len ; i += blkSze ) {
		cipher->encrypt( ctrBuf, tmpBuf );
		tmpBuf ^= pPlainText( i, blkSze );
		res.overwrite( i, tmpBuf );
		incrementCounter();
	}

	return res;

}

Buffer Crypto::CTR::decrypt(
	const	Buffer &	pCipherText ) {

	return encrypt( pCipherText );

}

// ============================================================================

Buffer Crypto::CTR::getiv() const {

	if ( ! cipher ) {
		throw NotInitialized();
	}

	return ctrBuf;

}

void Crypto::CTR::setiv(
	const	Buffer &	pIV ) {

	if ( ! cipher ) {
		throw NotInitialized();
	}

	if ( pIV.getLength() != blkSze ) {
		throw InvalidArgument();
	}

	ctrBuf = pIV;

}

// ============================================================================

void Crypto::CTR::incrementCounter() {

	if ( ctrBE ) {
		Uchar * ctrPtr = ctrBuf.getVarPtr() + blkSze - 1;
		for ( Uint32 x = blkSze ; x ; x-- ) {
			if ( ++(*ctrPtr--) ) {
				break;
			}
		}
	}
	else {
		Uchar * ctrPtr = ctrBuf.getVarPtr();
		for ( Uint32 x = blkSze ; x ; x-- ) {
			if ( ++(*ctrPtr++) ) {
				break;
			}
		}
	}

}

