// ============================================================================

Crypto::OFB::OFB() :

	Mode( getClassType() ) {

}

// ============================================================================

void Crypto::OFB::init(
		CipherPtr	pCipher,
	const	Buffer &	pIV,
	const	Buffer &	pKey ) {

	pCipher->setKey( pKey );
	pCipher->encrypt( pIV, IV );

	cipher	= pCipher;
	blkSze	= pCipher->getBlockSize();

}

void Crypto::OFB::done() {

	cipher.kill();

}

// ============================================================================

Buffer Crypto::OFB::encrypt(
	const	Buffer &	pPlainText ) {

	if ( ! cipher ) {
		throw NotInitialized();
	}

	Uint32 len = pPlainText.getLength();

	if ( len % blkSze ) {
		throw InvalidArgument();
	}

	Buffer ct;

	ct.resize( len );

	for ( Uint32 i = 0 ; i < len ; i += blkSze ) {
		ct.overwrite( i, pPlainText( i, blkSze ) ^ IV );
		cipher->encrypt( IV, IV );
	}

	return ct;

}

Buffer Crypto::OFB::decrypt(
	const	Buffer &	pCipherText ) {

	return encrypt( pCipherText );

}

// ============================================================================

Buffer Crypto::OFB::getiv() const {

	if ( ! cipher ) {
		throw NotInitialized();
	}

	return IV;

}

void Crypto::OFB::setiv(
	const	Buffer &	pIV ) {

	if ( ! cipher ) {
		throw NotInitialized();
	}

	if ( pIV.getLength() != blkSze ) {
		throw InvalidArgument();
	}
		
	cipher->encrypt( pIV, IV );

}

